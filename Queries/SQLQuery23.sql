SELECT INF.NUMERO, NF.IMPOSTO, INF.QUANTIDADE, FLOOR(SUM ( NF.IMPOSTO * (INF.PRECO * INF.QUANTIDADE))) 
FROM NOTAS_FISCAIS NF
JOIN ITENS_NOTAS_FISCAIS INF
ON NF.NUMERO = INF.NUMERO
WHERE YEAR(NF.DATA_VENDA) = '2016'
GROUP BY YEAR(NF.DATA_VENDA)

/* DESAFIO 
Na tabela de notas fiscais, temos o valor do imposto. 
Já na tabela de itens, temos a quantidade e o faturamento.
Calcule o valor do imposto pago no ano de 2016, 
arredondando para o menor inteiro.
*/

SELECT YEAR(NF.DATA_VENDA), SUM( NF.IMPOSTO * (INF.QUANTIDADE * INF.PRECO))
FROM NOTAS_FISCAIS NF
JOIN ITENS_NOTAS_FISCAIS INF
ON NF.NUMERO = INF.NUMERO
WHERE YEAR(NF.DATA_VENDA) = '2016'
GROUP BY YEAR(NF.DATA_VENDA)


